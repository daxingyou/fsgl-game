function LANGUAGE_ACTIVITY_LEFTTIME( _time )
	local timeStr = ""

	local day  = math.floor( _time / ( 60 * 60 * 24 ) )
	if not ( #timeStr == 0 and day == 0 ) then
		timeStr = timeStr..day.."天"
	end

	local hour = math.floor( _time / ( 60 * 60 ) ) % 24
	if not ( #timeStr == 0 and hour == 0 ) then
		timeStr = timeStr..hour.."小时"
	end

	local minute = math.floor( _time / 60 ) % 60
	if not ( #timeStr == 0 and minute == 0 ) then
		timeStr = timeStr..minute.."分钟"
	end

	local second = math.floor( _time ) % 60
	-- if not ( #timeStr == 0 and second == 0 ) then
		timeStr = timeStr..second.."秒"
	-- end

	return "活动剩余时间:"..timeStr
end

